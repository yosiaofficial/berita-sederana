            <p><strong>TUGAS DAN FUNGSI KOMUNIKASI INFORMASI PUBLIK</strong>
<li>Seksi Komunikasi dan Informasi Publik merupakan Satuan Kerja lini Suku Dinas dalam pelaksanaan pengelolaan komunikasi dan informasi publik di wilayah Kota Administrasi dan dipimpin oleh seorang Kepala Seksi yang berkedudukan di bawah dan bertanggung jawab kepada Kepala Suku Dinas.</li><br />

<strong>Seksi Komunikasi dan Informasi Publik mempunyai tugas :</strong><br>

<ol>
  <li>menyusun bahan rencana strategis dan rencana kerja dan anggaran Sulcu Dinas sesuai dengan lingkup tugasnya;</li>
  <li>melaksanakan rencana strategis dan dokumen pelaksanaan anggaran Suku Dinas sesuai dengan lingkup tugasnya;</li>
  <li>pelaksanaan kebijakan teknis pelaksanaan dalam bidang komunikasi dan informasi publik di wilayah Kota Administrasi;</li>
  <li>fasilitasi bimbingan teknis dan supervisi, serta pemantauan, evaluasi, dan pelaporan di bidang komunikasi dan informasi publik di wilayah Kota Administrasi;</li>
  <li>pengelolaan opini dan aspirasi publik di wilayah Kota Administrasi,</li>
  <li>pengelolaan dan pelayanan informasi publik di wilayah Kota Administrasi,</li>
  <li>penyediaan konten lintas sektoral dan pengelolaan media komunikasi publik di wilayah Kota Administrasi,</li>
  <li>layanan hubungan media di wilayah Kota Administrasi</li>
  <li>penguatan kapasitas sumber daya komunikasi publik dan penyediaan akses informasi di wilayah Kota Administrasi,</li>
  <li>mendukung pemanfaatan informasi publik melalui media luar ruang dan penerangan publik Kota Administrasi;</li>
  <li>melaksanakan fasilitasi alcses informasi publik terhadapKota Administrasi;</li>
  <li>melaksanakan pengumpulan, pengolahan dan penyajian data, informasi serta dokumentasi kegiatan pimpinan Kota Administrasi;</li>
  <li>melakukan penyuluhan langsung kepada masyarakat di tingkat Kota Administrasi;</li>
  <li>penyeldnggaraan layanan pengembangan dan pengelolaan aplikasi generik, spesifik dan suplemen yang terintegrasi di wilayah Kota Administrasi;</li>
  <li>melaporkan dan mempertanggungjawabkan pelaksanaan tugas Seksi Komunikasi dan Informasi Publik.</li>
</ol>
