            <p><strong>TUGAS DAN FUNGSI TATA USAHA</strong>
<li>Subbagian Tata Usaha merupakan Satuan Kerja staf Suku Dinas dalam pelaksanaan administrasi pada Suku Dinas dan dipimpin oleh seorang Kepala Subbagian yang berkedudukan di bawah dan bertanggung jawab kepada Kepala Suku Dinas.</li><br />

<strong>Subbagian Tata Usaha mempunyai tugas:</strong><br>

<ol>
  <li>menyusun bahan rencana strategis dan rencana kerja dan anggaran Suku Dinas sesuai dengan lingkup tugasnya;</li>
  <li>melaksanakan rencana strategis dan dokumen pelaksanaan anggaran Suku Dinas sesuai dengan lingkup tugasnya;</li>
  <li>mengoordinasikan penyusunan rencana strategis dan rencana kerja dan anggaran Suku Dinas;</li>
  <li>melaksanakan monitoring, pengendalian dan evaluasi pelaksanaan dokumen pelaksanaan anggaran Suku Dinas;</li>
  <li>melaksanakan pengelolaan kepegawaian;</li>
  <li>melaksanakan pengelolaan keuangan;</li>
  <li>melaksanakan pengelolaan barang;</li>
  <li>melaksanakan surat-menyurat dan kearsipan;</li>
  <li>memelihara dan merawat prasarana dan sarana kerja;</li>
  <li>memelihara kebersihan, keindahan, keamanan dan ketertiban kantor;</li>
  <li>melaksanakan pengelolaan ruang rapat/pertemuan Suku Dinas;</li>
  <li>melaksanakan publikasi kegiatan, upacara dan pengaturanacara Suku Dinas;</li>
  <li>menyiapkan bahan laporan Suku Dinas yang terkait dengan tugas Subbagian Tata Usaha;</li>
  <li>mengoordinasikan penyusunan laporan (kegiatan, keuangan, kinerja dan akuntabilitas) Suku Dinas; dan</li>
  <li>melaporkan dan mempertanggungjawabkan pelaksanaan tugas Subbagian Tata Usaha.</li>
</ol>
</p>
