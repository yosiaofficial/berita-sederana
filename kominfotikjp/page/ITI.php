            <p><strong>TUGAS DAN FUNGSI INFRASTRUKTURE TELEKOMUNIKASI INFORMATIKA</strong>
<li>Seksi Infrastruktur Telekomunikasi dan Informatika merupakan Satuan Kerja lini Suku Dinas dalam pelaksanaan pengelolaan, pembinaan, pembangunan dan pengembangan infrastruktur dan fasilitas penunjang teknologi infomasi di wilayah Kota Administrasi. Seksi Infrastruktur Telekomunikasi dan Informatika dipimpin oleh seorang Kepala Seksi yang berkedudukan di bawah dan bertanggung jawab kepada Kepala Suku Dinas.</li><br />

<strong>Seksi Infrastruktur Telekomunikasi dan Informatika mempunyai tugas:</strong><br>

<ol>
  <li>menyusun bahan rencana strategis dan rencana kerja dan anggaran Suku Dinas sesuai dengan lingkup tugasnya;</li>
  <li>melaksanakan rencana strategis dan dokumen pelaksanaan anggaran Suku Dinas sesuai dengan lingkup tugasnya;</li>
  <li>pelaksanaan kebijakan teknis di bidang informatika di wilayah Kota Administrasi;</li>
  <li>fasilitasi bimbingan teknis dan supervisi, serta pemantauan, evaluasi, dan pelaporan di bidang informatika di wilayah Kota Administrasi;</li>
  <li>penyelenggaraan layanan infrastruktur Data Center (DC) dan Teknologi Informasi dan Komunikasi (TIK) di wilayah Kota Administrasi;</li>
  <li>penyelenggaraan layanan pengembangan jaringan intranet dan penggunaan akses internet di wilayah Kota Administrasi;</li>
  <li>layanan kearnanan informasi e-Government di wilayah Kota Administrasi;</li>
  <li>layanan sistem komunikasi intra Pemerintah Daerah di wilayah Kota Administrasi;</li>
  <li>penyelenggaraan ekosistem Teknologi Informasi dan Komunikasi (TIK) Smart Province di wilayah Kota Administrasi;</li>
  <li>pengembangan sumber daya Teknologi Informasi dan Komunikasi (TIK) di wilayah Kota Administrasi;</li>
  <li>melaksanakan perencanaan, pembangunan, pengembangan dan pemeliharaan perangkat keras dan perangkat lunak teknologi informasi dan komunikasi di lingkungan Kota Administrasi;</li>
  <li>melaksanakan perencanaan, pembangunan, pengembangan dan pemeliharaan serta pengoperasian teknologi multimedia di lingkungan Kota Administrasi;</li>
  <li>melaksanakan perencanaan, pembangunan, pengembangan dan pemeliharaan perangkat jaringan dan sarana penunjang teknologi informasi dan komunikasi di lingkungan Kota Administrasi;</li>
  <li>melaksanakan pemantauan, pencatatan dan sosialisasi infrastruktur teknologi informasi dan komunikasi di lingkungan Kota Administrasi;</li>
  <li>melaksanakan penyusunan dan sosialisasi operasional standar prosedur, petunjuk teknis, petunjuk operasional, dan dokumentasi bidang teknologi informasi dan komunikasi di lingkungan Kota Administrasi;</li>
  <li>melaksanakan back up dan recovery terhadap data informasi dan sistem informasi, serta infrastruktur teknologi informasi dan komunikasi di lingkungan Kota Administrasi;</li>
  <li>melaksanakan pelatihan dan pengembangan wawasan terhadap publik dan aparatur dalam bidang pendayagunaan teknologi informasi dan komunikasi di lingkungan Kota Administrasi;</li>
  <li>melaksanakan evaluasi dan pengendalian pendayagunaan teknologi informasi dan komunikasi di lingkungan Kota Administrasi;</li>
  <li>melaksanakan koordinasi dan kerjasama teknis dalam perencanaan, pembangunan, pengembangan dan pemeliharaan bidang teknologi informasi dan komunikasi di lingkungan Kota Administrasi;</li>
  <li>melaksanakan konsultasi dan penanggulangan masalah , teknologi informasi dan komunikasi tingkat Kota Administrasi;</li>
  <li>melaporkan dan menyampaikan pelaksanaan tugas Seksi Infrastruktur Telekomunikasi dan Informatika.</li>
</ol>
